def load_upgrades(dump_path, race=None):
    """Build {engine_name: BuildItem(kind='upgrade')} merging engine times with REQUIREMENTS."""
    data = json.loads(Path(dump_path).read_text())
    by_name = {u["name"]: u for u in data.get("upgrades", [])}
    items = {}
    for ename, (r, bld, prereqs, _alias) in REQUIREMENTS.items():
        if race and r != race:
            continue
        u = by_name.get(ename)
        if u is None:
            continue  # not in this dump (era without the upgrade, or a name mismatch)
        items[ename] = BuildItem(
            name=ename, race=r, mineral_cost=u["mineral_cost"], gas_cost=u["gas_cost"],
            build_time_s=round(u["research_time_s"], 1), kind="upgrade",
            built_by=bld, requires=tuple(prereqs), time_verified=True)
    return items
