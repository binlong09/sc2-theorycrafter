"""Upgrade / research catalog — engine research times + GM-curated tech requirements.

The engine's UpgradeData gives authoritative research_time and cost, but NOT which
building researches an upgrade or its prerequisites. Those are GM-known and curated in
REQUIREMENTS. load_upgrades() merges the two into BuildItems the timing calculator can
schedule: kind='upgrade', built_by=<research building> (occupied for the research
duration), requires=<prereq buildings/upgrades that must exist first>.

Weapon/armor lines (L1/L2/L3) are generated by _levels() so the tier-2/3 building and
the prior-level prereq are consistent. Ability upgrades are listed explicitly.

Deferred (see TODO.md): PsiStorm (TemplarArchives not in the unit dump under that name),
and a handful of co-op-only upgrades. Names not present in a given era's dump are skipped.
"""

import json
