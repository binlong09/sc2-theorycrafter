def _levels(prefix, race, building, tier2=None, tier3=None, alias=""):
    """Generate L1/L2/L3 entries. L2 needs tier2 (if any) + L1; L3 needs tier3-or-tier2 + L2."""
    out = {}
    for lvl in (1, 2, 3):
        pre = []
        if lvl == 2 and tier2:
            pre.append(tier2)
        if lvl == 3 and (tier3 or tier2):
            pre.append(tier3 or tier2)
        if lvl >= 2:
            pre.append(f"{prefix}Level{lvl - 1}")
        out[f"{prefix}Level{lvl}"] = (race, building, tuple(pre), f"+{lvl} {alias}")
    return out


